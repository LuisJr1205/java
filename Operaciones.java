public class Operaciones {
    public static void main(String[] args) {
        int num_1 = 4;
        int num_2 = 4;
        int resul = 0;
        
        resul = num_1 + num_2 / 2;
        System.out.println("El valor de tu suma es:"+ resul);
        
        resul = (num_1 + num_2) / 2;
        System.out.println("El valor de tu suma es:"+ resul);
    }
}
